const LottieIcon = ({ name, isActive = false, size = 28 }) => {
  const animationData = iconAnimations[name];

  if (!animationData) return null;

  // Create a colored version based on active state
  const coloredData = JSON.parse(JSON.stringify(animationData));

  // Update colors in all layers
  const updateColors = (obj) => {
    if (Array.isArray(obj)) {
      obj.forEach(updateColors);
    } else if (obj && typeof obj === "object") {
      // Update stroke colors
      if (obj.ty === "st" && obj.c) {
        if (isActive) {
          obj.c = { a: 0, k: [185 / 255, 255 / 255, 102 / 255, 1] }; // lime #B9FF66
        } else {
          obj.c = { a: 0, k: [156 / 255, 163 / 255, 175 / 255, 1] }; // gray-400
        }
      }
      // Update fill colors
      if (obj.ty === "fl" && obj.c) {
        if (isActive) {
          obj.c = { a: 0, k: [185 / 255, 255 / 255, 102 / 255, 1] };
        } else {
          obj.c = { a: 0, k: [156 / 255, 163 / 255, 175 / 255, 1] };
        }
      }
      Object.values(obj).forEach(updateColors);
    }
  };

  updateColors(coloredData);

  return (
    <Lottie
      animationData={coloredData}
      loop={isActive}
      autoplay={isActive}
      style={{ width: size, height: size }}
    />
  );
};

export default LottieIcon;
